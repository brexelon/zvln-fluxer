#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn stable_snapshot_sha_matches() {
        assert_eq!(STABLE_SNAPSHOT.sha, STABLE_SNAPSHOT_SHA);
    }

    #[test]
    fn canary_snapshot_sha_matches() {
        assert_eq!(CANARY_SNAPSHOT.sha, CANARY_SNAPSHOT_SHA);
    }

    #[test]
    fn index_html_decodes_and_starts_with_doctype() {
        assert_index_html(&STABLE_SNAPSHOT);
        assert_index_html(&CANARY_SNAPSHOT);
    }

    #[test]
    fn sw_js_decodes_and_is_javascript() {
        assert_sw_js(&STABLE_SNAPSHOT);
        assert_sw_js(&CANARY_SNAPSHOT);
    }

    #[test]
    fn version_json_decodes_and_is_valid_json() {
        assert_version_json(&STABLE_SNAPSHOT);
        assert_version_json(&CANARY_SNAPSHOT);
    }

    fn assert_index_html(snapshot: &FrozenSnapshot) {
        let html = std::str::from_utf8(&snapshot.index_html).unwrap();
        assert!(
            html.starts_with("<!doctype html>"),
            "unexpected start: {}",
            &html[..40]
        );
    }

    fn assert_sw_js(snapshot: &FrozenSnapshot) {
        let js = std::str::from_utf8(&snapshot.sw_js).unwrap();
        assert!(
            js.contains("use strict"),
            "sw.js should contain 'use strict'"
        );
    }

    fn assert_version_json(snapshot: &FrozenSnapshot) {
        let json_str = std::str::from_utf8(&snapshot.version_json).unwrap();
        let parsed: serde_json::Value = serde_json::from_str(json_str).unwrap();
        assert!(
            parsed
                .get("sha")
                .or_else(|| parsed.get("version"))
                .is_some(),
            "version.json should have a sha or version field"
        );
    }
}
